#include <stdio.h>

int main(){

    //comentário de linha
    /*Comentário de bloco */
    //Váriaveis
    //tipo de variáveis
    //int(inteiro),float(reais),char(caracter)

    //Declaração de variáveis
    int inteiro;
    float reais;
    char caracter;

    //Declaração e inicialização
    char texto[20] = "C e facil!";

    //inicialização de variáveis
    inteiro = 10;
    reais = 3.14;
    caracter = 'S';

    //Pulando linha
    printf("Hello, World!\n\n");

    //Mostrando as variáveis
    printf("Inteiro: %d\n", inteiro);
    printf("Real: %f\n", reais);
    printf("Caracter: %c\n", caracter);
    printf("Texto: %s\n", texto);





    return 0;

}
